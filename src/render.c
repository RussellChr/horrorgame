/* ── Primitives ───────────────────────────────────────────────────────── */

void render_filled_rect(SDL_Renderer *r, int x, int y, int w, int h,
                        Uint8 red, Uint8 green, Uint8 blue, Uint8 alpha)
{
    SDL_SetRenderDrawColor(r, red, green, blue, alpha);
    SDL_FRect rect = { (float)x, (float)y, (float)w, (float)h };
    SDL_RenderFillRect(r, &rect);
}

void render_rect_outline(SDL_Renderer *r, int x, int y, int w, int h,
                         Uint8 red, Uint8 green, Uint8 blue, Uint8 alpha)
{
    SDL_SetRenderDrawColor(r, red, green, blue, alpha);
    SDL_FRect rect = { (float)x, (float)y, (float)w, (float)h };
    SDL_RenderRect(r, &rect);
}

void render_line(SDL_Renderer *r, int x1, int y1, int x2, int y2,
                 Uint8 red, Uint8 green, Uint8 blue, Uint8 alpha)
{
    SDL_SetRenderDrawColor(r, red, green, blue, alpha);
    SDL_RenderLine(r, (float)x1, (float)y1, (float)x2, (float)y2);
}

/* ── Bitmap text ──────────────────────────────────────────────────────── */

void render_text(SDL_Renderer *r, const char *text, int x, int y,
                 int scale, Uint8 red, Uint8 green, Uint8 blue)
{
    if (!text || scale < 1) return;
    SDL_SetRenderDrawColor(r, red, green, blue, 255);

    int cx = x;
    for (; *text; text++) {
        unsigned char c = (unsigned char)*text;
        if (c == '\n') {
            cx  = x;
            y  += 8 * scale + scale;
            continue;
        }
        if (c < 0x20 || c > 0x7E) { cx += 8 * scale; continue; }
        const unsigned char *bmp = font8x8[c - 0x20];
        for (int row = 0; row < 8; row++) {
            for (int col = 0; col < 8; col++) {
                if (bmp[row] & (0x80 >> col)) {
                    SDL_FRect px = {
                        (float)(cx + col * scale),
                        (float)(y  + row * scale),
                        (float)scale, (float)scale
                    };
                    SDL_RenderFillRect(r, &px);
                }
            }
        }
        cx += 8 * scale;
    }
}

void render_text_centered(SDL_Renderer *r, const char *text,
                          int cx, int y, int scale,
                          Uint8 red, Uint8 green, Uint8 blue)
{
    if (!text) return;
    int w = render_text_width(text, scale);
    render_text(r, text, cx - w / 2, y, scale, red, green, blue);
}

int render_text_width(const char *text, int scale)
{
    if (!text) return 0;
    int len = 0;
    for (; *text; text++) if (*text != '\n') len++;
    return len * 8 * scale;
}

int render_text_wrapped(SDL_Renderer *r, const char *text,
                        int x, int y, int max_width,
                        int scale, int line_h,
                        Uint8 red, Uint8 green, Uint8 blue)
{
    if (!text) return 0;
    int char_w   = 8 * scale;
    int chars_per_line = (max_width > 0 && char_w > 0)
                         ? max_width / char_w : 80;
    if (chars_per_line < 1) chars_per_line = 1;

    int lines = 0;
    char line_buf[256];
    const char *p = text;

    while (*p) {
        /* collect up to chars_per_line characters, break at word boundary */
        int count = 0;
        const char *word_end = p;
        const char *last_space = NULL;

        while (*word_end && count < chars_per_line) {
            if (*word_end == '\n') { word_end++; break; }
            if (*word_end == ' ') last_space = word_end;
            word_end++;
            count++;
        }

        /* if we ran out of room, roll back to last space */
        if (*word_end && last_space && count == chars_per_line) {
            word_end = last_space + 1;
            count = (int)(last_space - p);
        }

        if (count < 0) count = 0;
        if (count > 255) count = 255;
        memcpy(line_buf, p, (size_t)count);
        line_buf[count] = '\0';

        if (r) {
            render_text(r, line_buf, x, y + lines * line_h,
                        scale, red, green, blue);
        }
        lines++;
        p = word_end;
    }
    return lines;
}
